// Class Mhs (Blueprint untuk objek mahasiswa)
class Mhs {  
  String nim;  // Ini adalah Field untuk menyimpan NIM mahasiswa
  String nama;  // Ini adalah Field untuk menyimpan nama mahasiswa
  int semester;  // Ini adalah Field untuk menyimpan semester mahasiswa

  // Constructor untuk inisialisasi objek Mhs
  Mhs(this.nim, this.nama, this.semester);  // Menggunakan keyword 'this'
}